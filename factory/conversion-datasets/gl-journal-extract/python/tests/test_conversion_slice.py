"""Tests for the GL/journal conversion vertical slice.

All data is synthetic (see ../data, generated by make_synthetic_data.py). These
tests assert the factory's control evidence: byte-exact parsing, every reject
reason, dollar control totals, per-journal balance, and the load-gate behavior.
"""

from __future__ import annotations

from decimal import Decimal
from pathlib import Path

import pytest

from convert import run_slice, simulate_momentum_import
from gl_extract import FIELDS, RECORD_LENGTH, parse_extract, parse_record
from mapper import MomentumJournalLine, RejectedLine, _julian_to_iso, map_line

DATA = Path(__file__).resolve().parent.parent.parent / "data"


def read(name: str) -> str:
    return (DATA / name).read_text()


# --- parser ----------------------------------------------------------------


def test_field_map_is_contiguous_and_200_bytes():
    """The copybook byte map must tile [0, 200) with no gaps or overlaps."""
    ordered = sorted(FIELDS.values())
    assert ordered[0][0] == 0
    for (s1, e1), (s2, e2) in zip(ordered, ordered[1:]):
        assert e1 == s2, f"gap/overlap between {e1} and {s2}"
    assert ordered[-1][1] == RECORD_LENGTH == 200


def test_parse_trims_trailing_spaces_but_keeps_content():
    raw = parse_extract(read("gl_extract_clean.dat"))
    assert raw[0].jv_number == "000123"
    assert raw[0].dr_cr_ind == "D"
    assert raw[0].description == "UDO - MEDICAL SUPPLIES"
    assert raw[0].vendor_id == ""  # all-spaces vendor trims to empty


def test_short_record_is_padded_not_crashed():
    line = map_line(parse_record("2026", line_index=1))
    assert isinstance(line, RejectedLine)  # blank numerics rejected, no exception


# --- date conversion -------------------------------------------------------


@pytest.mark.parametrize(
    "ccyyddd,expected",
    [
        ("2026001", "2026-01-01"),
        ("2026032", "2026-02-01"),
        ("2024366", "2024-12-31"),  # 2024 is a leap year -> day 366 exists
    ],
)
def test_julian_to_iso_valid(ccyyddd, expected):
    assert _julian_to_iso(ccyyddd) == expected


@pytest.mark.parametrize("bad", ["2026000", "2026367", "2025366", "20260", "ABCDEFG"])
def test_julian_to_iso_invalid(bad):
    assert _julian_to_iso(bad) is None


# --- mapping / reject reasons ----------------------------------------------


def test_clean_batch_all_accepted_and_balanced():
    report, wire = run_slice(read("gl_extract_clean.dat"))
    assert report.lines_in == 6
    assert len(report.accepted) == 6
    assert report.rejected == []
    assert report.all_journals_balanced
    assert report.control_total_ok
    assert report.legacy_control_total == Decimal("400000.00")
    assert report.mapping_coverage == 1.0
    assert report.load_ready
    assert len(wire) == 6


def test_reject_reasons_are_exhaustive_and_isolated():
    report, _ = run_slice(read("gl_extract_with_rejects.dat"))
    assert report.row_accounting_ok
    assert report.lines_in == 12
    assert len(report.accepted) == 6
    assert len(report.rejected) == 6
    # Each synthetic reject line fires exactly one distinct reason.
    assert report.reject_reasons == {
        "BAD_DATE": 1,
        "BAD_DR_CR": 1,
        "BAD_FUND": 1,
        "BAD_USSGL": 1,
        "NON_NUMERIC": 1,
        "ZERO_AMOUNT": 1,
    }
    # Rejects must not disturb the accepted journals' balance or $ total.
    assert report.all_journals_balanced
    assert report.legacy_control_total == Decimal("400000.00")
    assert report.mapping_coverage == 0.5
    assert report.load_ready  # accepted set is still loadable


def test_unbalanced_journal_trips_the_gate():
    report, wire = run_slice(read("gl_extract_unbalanced.dat"))
    assert report.row_accounting_ok  # nothing dropped
    assert not report.all_journals_balanced
    assert [b.journal_id for b in report.unbalanced_journals] == ["JV-2026-000300"]
    assert not report.load_ready  # the whole point: invalid money cannot load
    # The $ control total still ties (we didn't lose money, the journal is just
    # internally unbalanced) — proving the two checks are independent.
    assert report.control_total_ok


def test_debit_credit_sign_assignment():
    report, _ = run_slice(read("gl_extract_clean.dat"))
    by_line = {(l.journal_id, l.line_number): l for l in report.accepted}
    debit = by_line[("JV-2026-000123", 1)]
    credit = by_line[("JV-2026-000123", 2)]
    assert debit.debit_amount == Decimal("100000.00")
    assert debit.credit_amount == Decimal("0.00")
    assert credit.credit_amount == Decimal("100000.00")
    assert credit.debit_amount == Decimal("0.00")


def test_fund_crosswalk_applied():
    report, _ = run_slice(read("gl_extract_clean.dat"))
    funds = {l.fund for l in report.accepted}
    assert funds == {"0160-OPS", "0162-MEDSVC"}  # legacy 0160/0162 mapped


# --- momentum import simulator (round trip) --------------------------------


def test_import_simulator_round_trips_balance():
    report, wire = run_slice(read("gl_extract_clean.dat"))
    sim = simulate_momentum_import(wire)
    assert sim["journals_loaded"] == 2
    assert sim["lines_loaded"] == 6
    assert sim["unbalanced_after_load"] == []
    assert sim["post_load_ok"] is True


def test_import_simulator_detects_unbalanced_after_load():
    _, wire = run_slice(read("gl_extract_unbalanced.dat"))
    sim = simulate_momentum_import(wire)
    assert sim["post_load_ok"] is False
    assert sim["unbalanced_after_load"] == ["JV-2026-000300"]
