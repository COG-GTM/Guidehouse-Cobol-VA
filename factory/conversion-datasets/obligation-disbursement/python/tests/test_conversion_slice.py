"""Tests for the obligation/disbursement conversion vertical slice.

All data is synthetic (see ../data, generated by make_synthetic_data.py). These
tests assert the factory's control evidence: byte-exact parsing, every reject
reason, dollar control totals, per-obligation funding (disbursements <=
obligation), and the load-gate behavior.
"""

from __future__ import annotations

from decimal import Decimal
from pathlib import Path

import pytest

from convert import _to_wire, run_slice, simulate_momentum_import
from mapper import (
    MomentumObligationLine,
    RejectedLine,
    _julian_to_iso,
    map_line,
)
from obl_extract import FIELDS, RECORD_LENGTH, parse_extract, parse_record

DATA = Path(__file__).resolve().parent.parent.parent / "data"


def read(name: str) -> str:
    return (DATA / name).read_text()


# --- parser ----------------------------------------------------------------


def test_field_map_is_contiguous_and_250_bytes():
    """The copybook byte map must tile [0, 250) with no gaps or overlaps."""
    ordered = sorted(FIELDS.values())
    assert ordered[0][0] == 0
    for (s1, e1), (s2, e2) in zip(ordered, ordered[1:]):
        assert e1 == s2, f"gap/overlap between {e1} and {s2}"
    assert ordered[-1][1] == RECORD_LENGTH == 250


def test_parse_trims_trailing_spaces_but_keeps_content():
    raw = parse_extract(read("obl_disbursement_clean.dat"))
    assert raw[0].obligation_no == "VA26OBL001"
    assert raw[0].txn_type == "O"
    assert raw[0].vendor_id == "V00012345"
    assert raw[0].description == "OBLIGATION - MEDICAL SUPPLIES"


def test_short_record_is_padded_not_crashed():
    line = map_line(parse_record("2026", line_index=1))
    assert isinstance(line, RejectedLine)  # blank numerics rejected, no exception


# --- date conversion -------------------------------------------------------


@pytest.mark.parametrize(
    "ccyyddd,expected",
    [
        ("2026001", "2026-01-01"),
        ("2026032", "2026-02-01"),
        ("2024366", "2024-12-31"),  # 2024 is a leap year -> day 366 exists
    ],
)
def test_julian_to_iso_valid(ccyyddd, expected):
    assert _julian_to_iso(ccyyddd) == expected


@pytest.mark.parametrize("bad", ["2026000", "2026367", "2025366", "20260", "ABCDEFG"])
def test_julian_to_iso_invalid(bad):
    assert _julian_to_iso(bad) is None


# --- mapping / reject reasons ----------------------------------------------


def test_clean_batch_all_accepted_and_funded():
    report, wire = run_slice(read("obl_disbursement_clean.dat"))
    assert report.lines_in == 5
    assert len(report.accepted) == 5
    assert report.rejected == []
    assert report.all_obligations_funded
    assert report.control_total_ok
    # $100k + $60k + $40k + $250k + $120k = $570k legacy dollars moved.
    assert report.legacy_control_total == Decimal("570000.00")
    assert report.mapping_coverage == 1.0
    assert report.load_ready
    assert len(wire) == 5


def test_reject_reasons_are_exhaustive_and_isolated():
    report, _ = run_slice(read("obl_disbursement_with_rejects.dat"))
    assert report.row_accounting_ok
    assert report.lines_in == 15
    assert len(report.accepted) == 5
    assert len(report.rejected) == 10
    # Each synthetic reject line fires exactly one distinct reason.
    assert report.reject_reasons == {
        "BAD_DATE": 1,
        "BAD_FUND": 1,
        "BAD_PERIOD": 1,
        "BAD_POP": 1,
        "BAD_TXN_TYPE": 1,
        "BAD_USSGL": 1,
        "MISSING_OBLIGATION_NO": 1,
        "MISSING_VENDOR": 1,
        "NON_NUMERIC": 1,
        "ZERO_AMOUNT": 1,
    }
    # Rejects must not disturb the accepted obligations' funding or $ total.
    assert report.all_obligations_funded
    assert report.legacy_control_total == Decimal("570000.00")
    assert report.mapping_coverage == round(5 / 15, 4)
    assert report.load_ready  # accepted set is still loadable


def test_over_disbursed_obligation_trips_the_gate():
    report, wire = run_slice(read("obl_disbursement_unbalanced.dat"))
    assert report.row_accounting_ok  # nothing dropped
    assert not report.all_obligations_funded
    assert [b.obligation_id for b in report.over_disbursed_obligations] == [
        "OB-2026-VA26OBL900"
    ]
    assert not report.load_ready  # the whole point: over-disbursement cannot load
    # The $ control total still ties (we didn't lose money, the obligation is
    # just over-disbursed) — proving the two checks are independent.
    assert report.control_total_ok


def test_obligation_disbursement_bucket_assignment():
    report, _ = run_slice(read("obl_disbursement_clean.dat"))
    by_line = {(l.obligation_id, l.line_number): l for l in report.accepted}
    obligation = by_line[("OB-2026-VA26OBL001", 1)]
    disbursement = by_line[("OB-2026-VA26OBL001", 2)]
    assert obligation.txn_type == "OBLIGATION"
    assert obligation.obligation_amount == Decimal("100000.00")
    assert obligation.disbursement_amount == Decimal("0.00")
    assert disbursement.txn_type == "DISBURSEMENT"
    assert disbursement.disbursement_amount == Decimal("60000.00")
    assert disbursement.obligation_amount == Decimal("0.00")


def test_partial_disbursement_leaves_remaining_balance():
    report, _ = run_slice(read("obl_disbursement_clean.dat"))
    balances = {b.obligation_id: b for b in report.obligation_balances}
    # OBL002 obligated $250k, disbursed $120k -> $130k remaining, still funded.
    ob2 = balances["OB-2026-VA26OBL002"]
    assert ob2.obligated == Decimal("250000.00")
    assert ob2.disbursed == Decimal("120000.00")
    assert ob2.remaining == Decimal("130000.00")
    assert ob2.is_funded


def test_fund_crosswalk_applied():
    report, _ = run_slice(read("obl_disbursement_clean.dat"))
    funds = {l.appropriation for l in report.accepted}
    assert funds == {"0160-OPS", "0162-MEDSVC"}  # legacy 0160/0162 mapped


