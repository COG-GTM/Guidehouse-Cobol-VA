"""Generate + validate the obligation/disbursement ICD from factory artifacts.

Usage:
    python generate_icd.py [--out-dir generated] [--date YYYY-MM-DD]

Emits three artifacts into ``--out-dir``:

    ICD-OBL-DISBURSEMENT.json   the structured document (machine-readable)
    ICD-OBL-DISBURSEMENT.md     readable Markdown rendering
    icd.data.js                 data file consumed by the portfolio dashboard

Exit code is non-zero if the generated ICD violates the customer's spec
(``validate.validate_icd``), so this doubles as a CI gate — the same pattern
as the conversion slices.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

from icd_builder import build_obligation_disbursement_icd
from validate import validate_icd


def _table(rows: list[dict], columns: list[str]) -> str:
    head = "| " + " | ".join(columns) + " |"
    sep = "| " + " | ".join("---" for _ in columns) + " |"
    body = []
    for row in rows:
        cells = []
        for col in columns:
            val = row.get(col, "")
            if isinstance(val, list):
                val = ", ".join(str(x) for x in val)
            cells.append(str(val).replace("|", "\\|"))
        body.append("| " + " | ".join(cells) + " |")
    return "\n".join([head, sep, *body])


def _bullets(items: list) -> str:
    return "\n".join(f"- {i}" for i in items)


