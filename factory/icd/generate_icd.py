def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out-dir", default=str(Path(__file__).parent / "generated"))
    parser.add_argument("--date", default=None, help="document date YYYY-MM-DD")
    args = parser.parse_args(argv)

    icd = build_obligation_disbursement_icd(document_date=args.date)
    violations = validate_icd(icd)

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "ICD-OBL-DISBURSEMENT.json").write_text(json.dumps(icd, indent=2) + "\n")
    (out_dir / "ICD-OBL-DISBURSEMENT.md").write_text(render_markdown(icd))
    (out_dir / "icd.data.js").write_text("window.ICD_DATA = " + json.dumps(icd, indent=2) + ";\n")

    n_fields = len(icd["file_mapping"])
    n_controls = len(icd["controls"])
    print(f"[generate_icd] sections rendered; file mapping fields: {n_fields}; controls: {n_controls}")
    if violations:
        print(f"[generate_icd] SPEC VIOLATIONS ({len(violations)}):")
        for item in violations:
            print(f"  - {item}")
        return 1
    print("[generate_icd] spec validation: PASS (0 violations against customer ICD schema)")
    print(f"[generate_icd] wrote {out_dir}/ICD-OBL-DISBURSEMENT.{{json,md}} and icd.data.js")
    return 0


if __name__ == "__main__":
    sys.exit(main())
